"""
Программа подсчёта количества этажей по нажатиям кнопок лифта
==============================================================
Вариант 17: Лифт Дилли

Описание задачи:
    В экспериментальном лифте только две кнопки:
      - Кнопка «1»: подняться на один этаж вверх
      - Кнопка «2»: опуститься на один этаж вниз
    Дилли катается на лифте, пока не побывает на каждом этаже хотя бы
    по одному разу. По последовательности нажатий определить общее
    количество этажей в доме.

Входные данные:
    Строка длиной от 1 до 100 символов, содержащая только '1' и '2'.

Выходные данные:
    Целое число — количество этажей в доме.

Примеры:
    "11"            -> 3
    "21212"         -> 2
    "1221221221221" -> 6

Ключевая идея:
    Если принять начальную позицию Дилли за 0, то каждое нажатие '1'
    смещает позицию на +1, а '2' — на -1. Общее количество этажей равно
    диапазону пройденных позиций: max_pos - min_pos + 1.

Автор: Тунда М.А.
Дата: 2026
"""


def validate_input(sequence: str) -> str:
    """
    Модуль ввода и валидации: проверяет корректность входной строки.

    Вход:
        sequence (str): необработанная строка нажатий кнопок.

    Выход:
        str: проверенная строка, готовая к обработке.

    Исключения:
        ValueError: если строка пустая, слишком длинная или содержит
                    недопустимые символы.

    Тесты (белый ящик):
        >>> validate_input("11") == "11"
        True
        >>> validate_input("") # => ValueError
        >>> validate_input("1" * 101) # => ValueError
        >>> validate_input("123") # => ValueError
    """
    if not sequence:
        raise ValueError("Строка нажатий не может быть пустой.")
    if len(sequence) > 100:
        raise ValueError(
            f"Строка слишком длинная: {len(sequence)} символов (максимум 100)."
        )
    for index, char in enumerate(sequence):
        if char not in ('1', '2'):
            raise ValueError(
                f"Недопустимый символ '{char}' на позиции {index}. "
                f"Допустимы только '1' и '2'."
            )
    return sequence
